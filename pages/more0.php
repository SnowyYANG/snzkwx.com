<h2>补充设定</h2>
<h3>创作来源</h3>
<p>我在中学的时候就有过一个似乎是自己成为望门寡、落难被救到日本的梦境和幻想，相关的梦境和妄想在22岁连续做落日梦境的时候变得详细。这些妄想在网友死讯传来后爆发，直到最近我偶尔也会梦见相关情节。这个作品是自己对这一系列妄想的整合。</p>
<h3>角色姓名</h3>
<p>金鸿燕的名字来源于我一个网友的梦，这位网友梦醒后还特意问我是不是叫这个名字。<br>
Hayashiya Yurika的名字是我在2017年的一个梦中听到的名字，当时听到的发音介于Yuriko和Yurika之间，我选择了Yurika，姓取自我在香港生活期间经常去的一家日料店的名字。<br>
李晓岚的名字完全来源于我本人的妄想，我在躁郁症发病期间特意想出了这个名字，但不排除是受《魔卡少女樱》男主角李小狼这一名字的影响。<br>
Kaede Haruki的姓参考了我一位患有血友病的网友的名字，而名是我随意取的。<br>
另外，金鸿燕在妓院的艺名是蝴蝶兰，这个艺名是我在香港植物园看到兜兰的英文名“<a target="_blank" href="https://ik.imagekit.io/snowyyang/snzkwx/slippery.jpg">Hong Kong Lady's Slippery Orchid</a>”（Slippery应该是Slipper，当时牌子多印了一个y）后产生的遐想，大陆有时称妓女为失足妇女。</p>
<h3>订婚</h3>
<p>恋爱三个月后，李晓岚和金鸿燕在夕阳下的街道中初吻，之后金鸿燕突然求婚，李晓岚惊讶了一下后现场答应。</p>
<h3>李晓岚的母亲不喜欢金鸿燕的理由</h3>
<p>李晓岚家境富有，在对自己的母亲提到订婚时，说是自己向金鸿燕求婚。在带金鸿燕去见母亲前，特意给金鸿燕买了很漂亮的新衣服和首饰。而李晓岚的母亲看到金鸿燕穿戴着华丽的衣服和首饰，觉得她生活不够节俭、爱花钱。之后金鸿燕又说出是她向李晓岚求婚，李晓岚的母亲就认定金鸿燕是看上了李晓岚的钱。</p>
<p>另一方面，李晓岚的父亲好女色，而李晓岚本人远离女色，李晓岚的母亲作为女人比起丈夫更喜欢儿子，金鸿燕占据了李晓岚的最爱，她也在内心深处感到嫉妒。</p>
<h3>金鸿燕的同学</h3>
<p>按照我的原始妄想，他也是金鸿燕的学长，但为了不和李晓岚学长混淆并节约篇幅而改为同学。他和金鸿燕在学校的实验室共同工作，他们的导师也很赞成他们走到一起。</p>
<p>他的舍友知道他原有婚约，但是舍友在他的要求下保密。中日战争时期舍友加入国民党军队成为空军，后阵亡。</p>
<h3>金鸿燕的母亲和弟弟</h3>
<p>金鸿燕失踪后，原本追求她的同学停止了对她家的资助并追债，最后母亲伤心病死，弟弟被托给亲戚，不久也因照顾不周而死亡。</p>
<h3>拐卖金鸿燕的人</h3>
<p>金鸿燕跳江后，有两位水手发现并立刻救起了金鸿燕。其中一位水手的母亲生病需要钱，在另一位水手的撺掇下一起把金鸿燕卖给了租界附近的妓院。另一位水手分赃后和他一拍两散，他花光了钱也没能将母亲的病治好，他一辈子都没讨到老婆。</p>
<h3>嗜睡症</h3>
<p>金鸿燕在妓院一个月内逐渐患上了嗜睡症，被男性触摸后会沉睡若干天，无法被他人唤醒。</p>
<h3>在妓院的生活</h3>
<p>金鸿燕在妓院多次流产，之后无法生育。<br>
经常做梦梦见李晓岚，梦里，李晓岚在得知金鸿燕被拐卖后受到打击而卧床不起，在金鸿燕多次梦中拜访后才能勉强工作。<br>
战争期间灵魂界也不太平，李晓岚的灵魂依然在为中国的未来而努力。<br>
因为经常做梦，金鸿燕在妓院除了唱歌跳舞、翻译还会占卜。为了不让鸨母分到钱，金鸿燕占卜不收现金只收奇奇怪怪难以典当的物品（这个妄想可能是受《XXXHolic》影响）。<br>
金鸿燕有同学在上海政府部门工作，但即使知道金鸿燕是被拐卖的，他也没法救出金鸿燕。</p>
<h3>金鸿燕赎身</h3>
<p>Kaede在上海逗留了两天，下棋是第一天，第二天他再次去妓院，金鸿燕向他展示了自己背部被鞭打的伤口，他才决定为金鸿燕赎身。</p>
<p>赎身时，Kaede让鸨母把金额写在纸上，Kaede出了大部分赎金，满铁的其他职员们出了小部分，但是还差一点点，另一位被拐卖的女孩子出了一点点。之后这位女孩被毒打。</p>
<h3>金鸿燕的和服</h3>
<p>满铁有一位职员一直追求金鸿燕，送了她一套振袖和服。金鸿燕唯一一次穿上这套和服是在Kaede接走金鸿燕的那天，到日本后Yurika卖掉了和服。</p>
<h3>Yuriko/Yurika</h3>
<p>原本金鸿燕的养父打算给她起名Yuriko，但是不近女色的他在当晚做梦梦见被Yuriko诱惑，于是他带金鸿燕去神社消厄祈福，最后给金鸿燕起名Yurika。</p>
<h3>Yurika的生活</h3>
<p>我不确定Yurika毕业后是否找到了工作，只是在原案中为了体现地震对经济的打击，让Yurika有了工作并因为地震失业。<br>
Yurika毕业后多次尝试给报社投稿，但一直没中。<br>
Yurika无聊喜欢爬树，养父经常叫她不要爬树。<br>
Yurika到日本后，无法再梦见李晓岚，但偶尔会梦见同学的原未婚妻，她也在日本，有当地的狼犬妖怪追求她。</p>
<h3>Yurika的丈夫</h3>
<p>Yurika的丈夫在与铁道相关的国有企业工作，战争后期仍然保证了家庭的基本生活。他在地震中丧偶，原配当时在做饭地震后被火烧死，两个孩子亲眼目睹了母亲的死亡并留下心理阴影，Yurika帮助两个孩子走出心理阴影。因为发生性关系后Yurika会睡着若干天，她和丈夫基本是无性婚姻。丈夫也尝试带Yurika去温泉旅馆度假，来治疗她的嗜睡症，Yurika告诉他原配因为被火烧伤，灵魂滞留在地狱，伤好后他们未来能再次转生相见，而Yurika仍爱着李晓岚，她追求的是永生永世的爱情。</p>
