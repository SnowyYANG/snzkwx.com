<h2> 10代の躁病妄想</h2>
<p>「TeenageManicDelusion」は、著者のSnowyの双極性障害の妄想に基づく歴史改変プロジェクトです。 <br>
オリジナルのケースは、3,300語の小説、コンセプトアート、ゲームです。 <br>
愛、戦争、お金、感情を含むエキゾチックなデュアル男性主人公の設定。 <br>
Snowyは2013年にこの計画を開始しました。10年近くの調整を繰り返した後、彼は2021年に元の計画を書き始め、2022年にそれを完成させました。 <br>
計画の目的は、妄想をできるだけ客観的に文書化することです。妄想を文書化することは、妄想が本物であるという意味でも、作者が物語の登場人物の行動を承認するという意味でもありません。 </p>